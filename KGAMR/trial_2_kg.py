class UMLSLinker:
    def __init__(self, api_key, db_path="umls_cache.db"):
        self.api_key = api_key
        self.db_path = db_path
        self.base_url = "https://uts-ws.nlm.nih.gov/rest"
        self._init_db()

    def _init_db(self):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute("""
                CREATE TABLE IF NOT EXISTS entity_cache (
                    term TEXT PRIMARY KEY,
                    cui TEXT,
                    semantic_type TEXT,
                    canonical_name TEXT
                )
            """)

    def get_cui(self, term):
        # 1. Check SQLite Cache
        with sqlite3.connect(self.db_path) as conn:
            res = conn.execute("SELECT cui, semantic_type, canonical_name FROM entity_cache WHERE term = ?", (term.lower(),)).fetchone()
            if res: return {"cui": res[0], "type": res[1], "name": res[2], "source": "cache"}

        # 2. Call UMLS API (Normalized Search)
        # We use pageSize=1 because your atomic entities are now very specific
        search_url = f"{self.base_url}/search/current"
        # Removed 'searchType': 'normalizedString' to broaden the search
        params = {'apiKey': self.api_key, 'string': term, 'pageSize': 1}

        try:
            time.sleep(0.1) # Rate limit safety
            r = requests.get(search_url, params=params)
            r.raise_for_status() # This will raise an HTTPError for 4xx/5xx responses
            results = r.json().get('result', {}).get('results', [])

            if results:
                cui = results[0]['ui']
                # 3. Fetch Details (Validation Phase)
                details = self.get_concept_details(cui)

                # Save to Cache
                with sqlite3.connect(self.db_path) as conn:
                    conn.execute("INSERT OR REPLACE INTO entity_cache VALUES (?, ?, ?, ?)",
                                (term.lower(), cui, details['type'], details['name']))

                return {"cui": cui, "type": details['type'], "name": details['name'], "source": "api"}
            else:
                print(f"No UMLS results found for term: {term}")
        except requests.exceptions.HTTPError as e:
            print(f"HTTP Error searching UMLS for {term}: {e}. Check your API Key and network connection.")
        except Exception as e:
            print(f"Linking Error for {term}: {e}")

        return None

    def get_concept_details(self, cui):
        url = f"{self.base_url}/content/current/CUI/{cui}"
        r = requests.get(url, params={'apiKey': self.api_key})
        data = r.json().get('result', {})
        return {
            "name": data.get('name'),
            "type": data.get('semanticTypes', [{}])[0].get('name', 'Unknown')
        }

