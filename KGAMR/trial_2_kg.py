                if 'rate limit' in err_str.lower() or '429' in err_str:
                    # daily quota message contains 'tokens per day' and a retry interval
                    if 'tokens per day' in err_str.lower():
                        # attempt to parse wait time in seconds from message
                        m = re.search(r"try again in ([0-9.]+)s", err_str)
                        wait = float(m.group(1)) if m else None
                        if wait:
                            print(f"Daily quota hit – sleeping for {wait:.1f}s before retrying...")
                            time.sleep(wait + 1)
                            backoff = 1.0
                            continue
                        else:
                            print("Daily quota hit and no wait interval parsed – aborting further attempts.")
                            raise DailyQuotaError()
                    if attempt < max_attempts:
                        print(f"Rate limit hit on attempt {attempt}, sleeping {backoff}s and retrying...")
