# 1. Define the Schema (The Blueprint)
class Entity(BaseModel):
    name: str = Field(description="The medical term/concept (e.g., 'Right Lower Lobe', 'PE')")
    category: Literal["Anatomy", "Pathology", "Finding", "Observation"] = Field(
        description="The category of the entity"
    )

class Triplet(BaseModel):
    subject: Entity
    predicate: str = Field(description="The relationship between entities (e.g., 'located_in', 'presents_as')")
    object: Entity
    assertion: Literal["Present", "Absent", "Indeterminate"] = Field(
        description="Whether the finding is confirmed or ruled out"
    )

class ExtractionResult(BaseModel):
    triplets: List[Triplet]

# 2. Example: Using the Schema to Parse LLM Output
