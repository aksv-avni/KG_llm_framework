class MedicalBasicValidator:
    def __init__(self, cache_db="umls_cache.db"):
        self.cache_db = cache_db
        self._init_validation_db()

    def _init_validation_db(self):
        with sqlite3.connect(self.cache_db) as conn:
            conn.execute("""
                CREATE TABLE IF NOT EXISTS validation_cache (
                    id INTEGER PRIMARY KEY AUTOINCREMENT,
                    entity_mention TEXT,
                    entity_cui TEXT,
                    entity_umls_type TEXT,
                    entity_category TEXT,
                    is_valid INTEGER,
                    review_status TEXT,
                    errors TEXT,
                    warnings TEXT
                )
            """)

    def validate_entity(self, entity_data, source_id):
        """
        Validates an entity against the structural rules.
        entity_data: Dictionary containing 'mention', 'umls_cui', 'umls_type', 'category' for the entity.
        source_id: Identifier for the source of the entity (e.g., 'subject' or 'object').
        """
        mention = entity_data.get('mention', '').strip()
        cui = entity_data.get('umls_cui', 'N/A')
        umls_type = entity_data.get('umls_type', 'N/A')
        category = entity_data.get('category', '')

        errors = []
        warnings = []
        is_valid = True

        # Rule 1: Entity mention text not empty (unless CUI is 'N/A', meaning it wasn't extracted at all)
        if not mention and cui != 'N/A':
            errors.append("Rule 1: Entity mention text is empty.")
            is_valid = False

        # Rule 2: CUI Format (C followed by 7 digits) if CUI is present (not 'N/A')
        if cui != 'N/A':
            if not re.match(r"^C\d{7}$", cui):
                errors.append(f"Rule 2: Invalid CUI format '{cui}'.")
                is_valid = False
        # If CUI is 'N/A', it means it wasn't linked, which might be acceptable but not 'valid' for a grounded entity
        else:
            warnings.append("Rule 2: Entity could not be linked to UMLS (CUI is 'N/A').")

        # Rule 3: Entities with CUI have semantic types (if CUI is present and not 'N/A')
        if cui != 'N/A' and umls_type == 'N/A':
            errors.append("Rule 3: Grounded entity missing semantic type (UMLS Type is 'N/A' but CUI is present).")
            is_valid = False

        # If there are errors, the entity is not valid
        if errors:
            is_valid = False

        # Build Result
        result = {
            "is_valid": is_valid,
            "entity": entity_data,
            "errors": errors,
            "warnings": warnings,
            "source_id": source_id
        }

        # Failure Handling & Caching
        self._store_validation_result(result)

        return result

    def _store_validation_result(self, res):
        """Stores result in cache for human review"""
        status = "done" if res['is_valid'] and not res['warnings'] else "pending"
        with sqlite3.connect(self.cache_db) as conn:
            conn.execute("INSERT INTO validation_cache (entity_mention, entity_cui, entity_umls_type, entity_category, is_valid, review_status, errors, warnings) VALUES (?, ?, ?, ?, ?, ?, ?, ?)", (
                res['entity'].get('mention'),
                res['entity'].get('umls_cui'),
                res['entity'].get('umls_type'),
                res['entity'].get('category'),
                1 if res['is_valid'] else 0,
                status,
                "; ".join(res['errors']),
                "; ".join(res['warnings'])
            ))

