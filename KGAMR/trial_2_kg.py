"""### Function to Clean Report Text

This function will preprocess the raw clinical report by removing unnecessary characters and normalizing whitespace to provide a cleaner input for the LLM. This can help the LLM focus on the relevant medical terminology and structures.
"""

import re

def clean_report_text(text):
    # Remove timestamps like <TIME>, <DATE>, <HCW>
    cleaned_text = re.sub(r'<TIME>|<DATE>|<HCW>', '', text)
    # Replace '/' with ' or '
    cleaned_text = re.sub(r'/', ' or ', cleaned_text)
    # Remove multiple spaces, newlines, and tabs, replace with single space
    cleaned_text = re.sub(r'\s+', ' ', cleaned_text).strip()
    # Remove leading/trailing colons or numbering from report sections
    cleaned_text = re.sub(r'^[:\d\s.]+', '', cleaned_text)
    # Remove any stray punctuation that might interfere with extraction, keeping only periods, commas, and hyphens relevant to medical terms
    # The 'or' from the previous step is composed of letters and spaces, so it will be preserved by this regex.
    cleaned_text = re.sub(r'[^a-zA-Z0-9.,\- ]', '', cleaned_text)
    # Normalize hyphens if needed (e.g., multiple hyphens to single)
    cleaned_text = re.sub(r'-+', '-', cleaned_text)
    # Remove any extra spaces around punctuation
    cleaned_text = re.sub(r'\s*([.,-])\s*', r'\1', cleaned_text)
    return cleaned_text




